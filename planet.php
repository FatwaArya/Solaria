      </div>
      <div class="desc">
          <p>
            On a dark, clear night, it is usually easy to see a dusty white band of stars stretching across the sky. This is the Milky Way galaxy, a massive collection of stars, dust, and clouds of gas. The Milky Way contains hundreds of billions of stars. It is only one of billions of galaxies in the universe.

The Sun is one of the stars in the Milky Way. Therefore, Earth and all the other planets in the solar system are also in the Milky Way. Just as the planets revolve around the Sun, the stars in the Milky Way revolve around its nucleus, or center. It takes the Sun about 200 million years to travel around the nucleus of the Milky Way.

Viewed from above, the Milky Way looks like a giant pinwheel. The stars are gathered in the flat shape of a disk, and arms of stars spiral out from the nucleus. The nucleus is a bulging cluster of stars. It has a black hole at the center. (A black hole is an area with such a strong force of gravity that light cannot escape from it.)

The Sun and its planets are located on the inner edge of one of the Milky Way’s arms. The solar system is about 30,000 light-years away from the center of the galaxy. (A light-year is the distance that light travels in one year—about 5.8 trillion miles, or 9.5 trillion kilometers.) The whole galaxy is about 100,000 light-years across.
